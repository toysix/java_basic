package homework0407;

public class Sameple3_1 {
	
	public static void main(String[] args) {
		
		int a23 = 10;
		int b23 = 5;
		
		System.out.println(a23+b23);
		System.out.println(a23-b23);
		System.out.println(a23*b23);
		System.out.println(a23/b23);
		
		System.out.println(7%3);
		System.out.println(3%7);
		
		int i23 = 0;
		int j23 = 10;
		
		i23++;
		j23--;
		
		System.out.println(i23);
		System.out.println(j23);
		
		System.out.println(i23++);
		System.out.println(i23);
		
		System.out.println(++i23);
		System.out.println(i23);
		
		
		
	}

}
